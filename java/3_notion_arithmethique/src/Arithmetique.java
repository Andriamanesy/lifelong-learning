public class Arithmetique
{
	public static void main(String argc[])
	{
		System.out.println("On va utiliser les operateur +, -, /, *, et %\n");

		System.out.println("Ici on fait l'addition de variable a = 10 et b = 5");
		short a;
		short b;
		a = 10;
		b = 5;
		System.out.println("Le resultat est "+(a+b)+"\n");

	
		System.out.println("Ici on fait la soustraction de variable c = 10 et d = 5");
		short c;
		short d;
		c = 10;
		d = 5;
		System.out.println("Le resultat est "+(c-d)+"\n");


		System.out.println("Ici on fait la division de variable e = 10 et f = 5");
		short e;
		short f;
		e = 10;
		f = 5;
		System.out.println("Le resultat est "+(e/f)+"\n");


		System.out.println("Ici on fait la multiplication de variable g = 10.5 et h = 5.25");
		float g;
		float h;
		g = 10.5f;
		h = 5.25f;
		System.out.println("Le resultat est "+(g*h)+"\n");


		System.out.println("Ici on fait le modulo des variables i = 10 et j = 5");
		short i;
		short j;
		i = 10;
		j = 5;
		System.out.println("Le resultat est "+(i%j)+"\n");
	}
}
